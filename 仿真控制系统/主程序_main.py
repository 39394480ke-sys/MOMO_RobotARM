def 处理命令(
    输入内容: str,
    机械臂: 机械臂模型,
    姿态管理: 姿态管理器,
    动作播放: 动作播放器,
) -> bool:
    """解析并执行一行命令。返回 True 表示退出程序。"""

    命令片段 = 输入内容.split()
    命令 = 命令片段[0]
    参数 = 命令片段[1:]
    命令名 = 标准化命令(命令)

    if 命令名 == "帮助":
        打印帮助()
        return False

    if 命令名 == "退出":
        print("已退出仿真控制系统。")
        return True

    if 命令名 == "状态":
        打印状态(机械臂)
        return False

    if 命令名 == "移动":
        执行移动(参数, 机械臂)
        return False

    if 命令名 == "移动单关节":
        执行移动单关节(参数, 机械臂)
        return False

    if 命令名 == "回家":
        结果 = 机械臂.回到默认姿态()
        print(结果.消息)
        if 结果.成功:
            打印状态(机械臂)
        return False

    if 命令名 == "一键回零":
        姿态 = 姿态管理.获取姿态("初始姿态")
        if 姿态 is None:
            结果 = 机械臂.回到默认姿态()
        else:
            姿态 = dict(姿态)
            姿态.pop("夹爪", None)
            结果 = 机械臂.应用姿态(姿态)
        print("一键回零完成。" if 结果.成功 else f"一键回零失败：{结果.消息}")
        if 结果.成功:
            打印状态(机械臂)
        return False

    if 命令名 == "夹爪":
        执行夹爪(参数, 机械臂)
        return False

    if 命令名 == "保存姿态":
        执行保存姿态(参数, 机械臂, 姿态管理)
        return False

    if 命令名 == "前往姿态":
        执行前往姿态(参数, 机械臂, 姿态管理)
        return False

    if 命令名 == "姿态列表":
        打印名称列表("姿态列表", 姿态管理.列出姿态())
        return False

    if 命令名 == "删除姿态":
        执行删除姿态(参数, 姿态管理)
        return False

    if 命令名 == "动作列表":
        打印名称列表("动作列表", 动作播放.列出动作())
        return False

    if 命令名 == "播放动作":
        执行播放动作(参数, 动作播放, 机械臂)
        return False

    print(f"未知命令：{命令}")
    print("输入“帮助”查看可用命令。")
    return False


