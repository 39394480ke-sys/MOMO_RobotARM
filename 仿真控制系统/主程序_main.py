def 主函数() -> None:
    """程序入口：创建模块并进入命令循环。"""

    try:
        配置 = read_structured(配置路径)
        准备默认文件(配置)

        机械臂 = 机械臂模型(配置)
        姿态管理 = 姿态管理器(
            当前目录 / 配置["文件"]["姿态库"],
            默认姿态=配置.get("默认姿态", {}),
        )
        动作配置 = 配置.get("动作播放", {})
        动作播放 = 动作播放器(
            机械臂,
            当前目录 / 配置["文件"]["动作目录"],
            默认等待秒=float(动作配置.get("默认等待秒", 0.15)),
            默认插值步数=int(动作配置.get("默认插值步数", 5)),
        )
    except Exception as 错误:
        print(f"启动失败：{错误}")
        sys.exit(1)

    打印欢迎信息()
    打印状态(机械臂)

    while True:
        try:
            输入内容 = input("\n请输入命令 > ").strip()
        except (EOFError, KeyboardInterrupt):
            print("\n已退出仿真控制系统。")
            break

        if not 输入内容:
            continue

        是否退出 = 处理命令(输入内容, 机械臂, 姿态管理, 动作播放)
        if 是否退出:
            break


def 准备默认文件(配置: dict[str, Any]) -> None:
    """首次运行时创建姿态库和动作库。"""

    文件配置 = 配置.get("文件", {})
    姿态库路径 = 当前目录 / 文件配置.get("姿态库", "姿态库.json")
    动作目录 = 当前目录 / 文件配置.get("动作目录", "动作库")

    动作目录.mkdir(parents=True, exist_ok=True)

    if not 姿态库路径.exists():
        默认姿态库 = {}
        for 名称, 姿态 in 配置.get("默认姿态", {}).items():
            默认姿态库[名称] = {
                "关节角度": 姿态.get("关节角度", [0, 0, 0, 0, 0]),
                "夹爪": 姿态.get("夹爪", 50),
                "说明": 姿态.get("说明", ""),
            }
        write_json(姿态库路径, 默认姿态库)

    挥手路径 = 动作目录 / "挥手.json"
    if not 挥手路径.exists():
        write_json(挥手路径, 默认挥手动作())


