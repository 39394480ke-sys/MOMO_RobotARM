def 执行移动(参数: list[str], 机械臂: 机械臂模型) -> None:
    """执行“移动 0 0 20 30 10 0”。"""

    try:
        角度 = [float(值) for 值 in 参数]
    except ValueError:
        print("移动失败：角度必须是数字。示例：移动 0 0 20 30 10 0")
        return

    结果 = 机械臂.移动到关节角度(角度)
    print(结果.消息)
    if 结果.成功:
        打印状态(机械臂)


def 执行移动单关节(参数: list[str], 机械臂: 机械臂模型) -> None:
    """执行“移动单关节 2 30”。"""

    if len(参数) != 2:
        print("移动单关节失败：格式应为：移动单关节 关节编号 目标角度")
        return

    try:
        关节编号 = int(参数[0])
        目标角度 = float(参数[1])
    except ValueError:
        print("移动单关节失败：关节编号必须是整数，目标角度必须是数字。")
        return

    结果 = 机械臂.移动单个关节(关节编号, 目标角度)
    print(结果.消息)
    if 结果.成功:
        打印状态(机械臂)


def 执行夹爪(参数: list[str], 机械臂: 机械臂模型) -> None:
    """执行夹爪命令。"""

    if len(参数) != 1:
        print("夹爪命令格式：夹爪 0-100，或：夹爪 张开，夹爪 闭合")
        return

    目标 = 参数[0]
    if 目标 in {"张开", "打开", "open"}:
        结果 = 机械臂.张开夹爪()
    elif 目标 in {"闭合", "关闭", "close"}:
        结果 = 机械臂.闭合夹爪()
    else:
        try:
            结果 = 机械臂.设置夹爪(float(目标))
        except ValueError:
            print("夹爪命令失败：请输入 0 到 100 的数字，或“张开”“闭合”。")
            return

    print(结果.消息)
    if 结果.成功:
        打印状态(机械臂)


def 执行保存姿态(参数: list[str], 机械臂: 机械臂模型, 姿态管理: 姿态管理器) -> None:
    """保存当前状态为一个姿态。"""

    if not 参数:
        print("保存姿态失败：请提供姿态名称。示例：保存姿态 A姿态")
        return

    名称 = " ".join(参数)
    try:
        姿态管理.保存姿态(名称, 机械臂.获取当前状态(), 说明="用户在命令行保存的姿态。")
    except ValueError as 错误:
        print(f"保存姿态失败：{错误}")
        return

    print(f"保存成功：{名称}")


def 执行前往姿态(参数: list[str], 机械臂: 机械臂模型, 姿态管理: 姿态管理器) -> None:
    """移动到姿态库中的命名姿态。"""

    if not 参数:
        print("前往姿态失败：请提供姿态名称。示例：前往姿态 初始姿态")
        return

    名称 = " ".join(参数)
    姿态 = 姿态管理.获取姿态(名称)
    if 姿态 is None:
        print(f"前往姿态失败：没有找到姿态“{名称}”。")
        return

    结果 = 机械臂.应用姿态(姿态)
    if 结果.成功:
        print(f"已移动到：{名称}")
        打印状态(机械臂)
    else:
        print(f"前往姿态失败：{结果.消息}")


def 执行删除姿态(参数: list[str], 姿态管理: 姿态管理器) -> None:
    """删除一个命名姿态。"""

    if not 参数:
        print("删除姿态失败：请提供姿态名称。")
        return

    名称 = " ".join(参数)
    if 姿态管理.删除姿态(名称):
        print(f"已删除姿态：{名称}")
    else:
        print(f"删除姿态失败：没有找到姿态“{名称}”。")


def 执行播放动作(参数: list[str], 动作播放: 动作播放器, 机械臂: 机械臂模型) -> None:
    """播放动作库中的动作。"""

    if not 参数:
        print("播放动作失败：请提供动作名称。示例：播放动作 挥手")
        return

    名称 = " ".join(参数)
    print(f"开始播放动作：{名称}")
    结果 = 动作播放.播放动作(名称, 打印回调=print)
    print(结果.消息)
    if 结果.成功:
        打印状态(机械臂)


def 打印状态(机械臂: 机械臂模型) -> None:
    """打印当前机械臂状态。"""

    状态 = 机械臂.获取详细状态()
    print("\n当前状态：")
    for 关节 in 状态["关节"]:
        print(
            f"{关节['编号']}. {关节['名称']}：{格式化数值(关节['角度'])} 度 "
            f"(范围 {格式化数值(关节['最小角度'])} 到 {格式化数值(关节['最大角度'])}，{关节['模式']})"
        )
    print(f"夹爪：{格式化数值(状态['夹爪'])} (范围 {格式化数值(状态['夹爪最小'])} 到 {格式化数值(状态['夹爪最大'])})")


def 打印名称列表(标题: str, 名称列表: list[str]) -> None:
    """打印姿态或动作名称列表。"""

    print(f"\n{标题}：")
    if not 名称列表:
        print("  暂无")
        return

    for 名称 in 名称列表:
        print(f"  - {名称}")


